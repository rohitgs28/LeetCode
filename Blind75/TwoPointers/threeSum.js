/**
 * 15. 3Sum
 * https://leetcode.com/problems/3sum/
 *
 * Sort + Two Pointer approach — O(n²) time, O(1) extra space
 * 
 * Sort the array, then for each element, use two pointers on the
 * remaining subarray to find pairs that sum to the target.
 * Skip duplicates at each level to avoid duplicate triplets.
 *
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  const result = [];
  nums.sort((a, b) => a - b);

  for (let i = 0; i < nums.length - 2; i++) {
    // Skip duplicate values for the first element
    if (i > 0 && nums[i] === nums[i - 1]) continue;

    // Early termination: if smallest possible sum > 0, no more solutions
    if (nums[i] > 0) break;

    let left = i + 1;
    let right = nums.length - 1;

    while (left < right) {
      const sum = nums[i] + nums[left] + nums[right];

      if (sum === 0) {
        result.push([nums[i], nums[left], nums[right]]);

        // Skip duplicates for both pointers
        while (left < right && nums[left] === nums[left + 1]) left++;
        while (left < right && nums[right] === nums[right - 1]) right--;

        left++;
        right--;
      } else if (sum < 0) {
        left++;
      } else {
        right--;
      }
    }
  }

  return result;
};
